unsigned int CRC16(char *data, int size)
{
    unsigned int chk = 0;
    int i;
    for (i = 0; i < size; i++)
    {
        chk = (crc16_table[(data[i] ^ chk) & 0xFF] ^ chk >> 8);
    }
    return chk;
}

int main(int argc, char** argv)
{
	if(argc < 2)
	{
		printf("Argument error!\n");
		return 0;
	}

	FILE *sav = fopen(argv[1], "rwb+");
	if (!sav)
	{
		printf("Reading failed!\n");
		return 0;
	}

	fseek(sav, 0, SEEK_END);
	int size = ftell(sav);
	rewind(sav);

    char *pkr = (char *)malloc(0xC);
    char pkr2[] = { 0x50, 0x4B, 0x52, 0x2D, 0x30, 0x35, 0x39, 0x00, 0x61, 0x30, 0x30, 0x00 }; // PKR-059 a00
    fread(pkr, 1, 0xC, sav);
	rewind(sav);

	if((size == 0x40000 || size == 0x4007A || size == 0x10000 || size == 0x1007A) && !strcmp(pkr, pkr2)) // Jap: 256kb, EU/US: 65kb
    {
        unsigned int *saveinfo = (unsigned int *)malloc(0x1F4);
        fseek(sav, 0xC, SEEK_SET);
        fread(saveinfo, 1, 0x1F4, sav);

        if(saveinfo[1] >> 16 != 0)
        {
            RangerCrypto(saveinfo, 0x7D);
            fseek(sav, 0xC, SEEK_SET);
            fwrite(saveinfo, 1, 0x1F4, sav);
        }
        else
        {
            unsigned int *tmp = (unsigned int *)malloc(0x1F4);
            memcpy(tmp, saveinfo, 0x1F4);

            if(tmp[1] >> 16 == 0) // if decrypted fix checksum before encrypting
            {
                tmp[1] = CRC16((char*)tmp+8, 0x1EC);
            }

            RangerCrypto(tmp, 0x7D);
            fseek(sav, 0xC, SEEK_SET);
            fwrite(tmp, 1, 0x1F4, sav);
            free(tmp);
        }

        unsigned int *saveinfo2 = (unsigned int *)malloc(0x1F4);
        fseek(sav, 0x20C, SEEK_SET);
        fread(saveinfo2, 1, 0x1F4, sav);

        if(saveinfo2[1] >> 16 == 0) // if decrypted fix checksum before encrypting
        {
            saveinfo2[1] = CRC16((char*)saveinfo2+8, 0x1EC);
        }

        RangerCrypto(saveinfo2, 0x7D);
        fseek(sav, 0x20C, SEEK_SET);
        fwrite(saveinfo2, 1, 0x1F4, sav);
        free(saveinfo2);

        int c;
        for(c = 0; c < 10; c++)
        {
            unsigned int chunknumber = saveinfo[0xC + (c*0xC)] >> 16 & 0xFF;
            unsigned int chunkoffset = saveinfo[0xD + (c*0xC)];
            unsigned int datasize = saveinfo[0xE + (c*0xC)];
            unsigned int chunksize = saveinfo[0xF + (c*0xC)] / chunknumber;

            if(chunkoffset != 0 && chunksize <= 0x40000)
            {
                int i;
                for(i = 0; (i < chunknumber) && (i < 3); i++)
                {
                    unsigned int *chunk = (unsigned int *)malloc(chunksize);

                    fseek(sav, chunkoffset + (chunksize*i), SEEK_SET);
                    fread(chunk, 1, chunksize, sav);

                    if(chunk[1] >> 16 == 0 && chunk[1] & 0xFFFF != 0) // if decrypted fix checksum before encrypting
                    {
                        chunk[1] = CRC16((char*)chunk+0x18, datasize);
                    }

                    RangerCrypto(chunk, ((datasize+0x18)/4));

                    fseek(sav, chunkoffset + (chunksize*i), SEEK_SET);
                    fwrite(chunk, 1, chunksize, sav);

                    free(chunk);
                }
            }
        }

        free(saveinfo);
    }

	fclose(sav);
	
	printf("Done!\n");

	return 0;
}
